void	add_int_prefix(char **str, int d, t_format *f)
{
	if (d < 0)
		(*str)[0] = '-';
	else if (f->plus)
		(*str)[0] = '+';
	else if (f->sp)
		(*str)[0] = ' ';
	else
		return ;
	(*str)++; 
}
